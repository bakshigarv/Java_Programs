public class Ifelse {
    public static void main(String[]args){
        
        int myInt = 15;
        
        if (myInt < 10) {
            System.out.println("Yes, 1st one is true!");
        }
        else if(myInt > 20){
            System.out.println("Yes, 2nd one is true!");
        }
        else{
            System.out.println("None of the above is true!");
        }
    }
}